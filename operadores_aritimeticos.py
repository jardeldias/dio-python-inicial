produto_1 = 20
produto_2 = 10

# Soma
print(produto_1 + produto_2)
# Subtração
print(produto_1 - produto_2)
# Divisão
print(produto_1 / produto_2)
# Divisão inteira
print(produto_1 // produto_2)
# Multiplicação
print(produto_1 * produto_2)
# Módulo, resto da divisão
print(produto_1 % produto_2)
# Exponenciação
print(produto_1 ** produto_2)
