saldo = 200
print(saldo)

# Recebe o valor da variável ... valor atribuido
saldo += 10
print(saldo)

saldo -= 5
print(saldo)

# Funciona apenas com números inteiro, não floats
saldo //= 2
print(saldo)

saldo /= 2
print(saldo)

saldo *= 10
print(saldo)

saldo **= 2
print(saldo)

saldo %= 3
print(saldo)
